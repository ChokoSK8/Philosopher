#include "philo.h"

int	check_error(int ac, char **av)
{
	int	c;

	if (ac < 5 || ac > 6)
		return (1);
	c = 1;
	while (av[c])
	{
		if (ft_atoi(av[c]) < 0)
			return (1);
		c++;
	}
	return (0);
}

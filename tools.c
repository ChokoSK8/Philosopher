#include "philo.h"

long int	ft_diff_time(struct timeval t0, struct timeval t1)
{
	long int	ret;

	ret = (t1.tv_sec * 1000 + t1.tv_usec / 1000
			- (t0.tv_sec * 1000 + t0.tv_usec / 1000));
	return (ret);
}
